# Mendefinisikan tuple dengan nilai-nilai yang diberikan
nilai = (80, 90, 85, 60, 75)

# Menjumlahkan semua nilai dalam tuple
total = sum(nilai)

# Menghitung jumlah elemen dalam tuple
jumlah_elemen = len(nilai)

# Menghitung rata-rata
rata_rata = total / jumlah_elemen

# Mencetak hasil
print("Nilai rata-rata adalah:", rata_rata)
